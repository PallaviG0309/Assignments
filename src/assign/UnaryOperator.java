package assign;

public class UnaryOperator {

	public static void main(String[] args) 
	{
		int i1 = -10;
	      System.out.println(i1); //this will print -10 bcoz we are given i1 values is -10 

	      int i2 = 10;
	      System.out.println(i2++);// this will print 10 bcoz i2 value is 10 and will print 10 

	      int i3 = 10;
	      System.out.println(++i3);// this will print 11 bcoz i3 value is 10 will increase by 1  

	      int i4 = 10;
	      System.out.println(i4--);//this will print 10 bcoz it will first assign then decrese

	      int i5 = 10;
	      System.out.println(--i5);//this will print 9 bcoz i5 value is 10 first decrese value and then assign

	  /*    int i6 = 10;
	      System.out.println(~i6);//this will print -11*/

	}

}
